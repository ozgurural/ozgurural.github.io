/* ==========================================================================
   Site search — a lunr.js overlay.

   Standalone classic script (no ES import/export), loaded by scripts.html
   ONLY when `search: true` in _config.yml. Both lunr and the search index
   load lazily on the first open, so normal page loads carry zero extra
   weight. Styling lives in /_sass/_search.scss; the index is generated by
   /search.json.
   ========================================================================== */
(function () {
  "use strict";

  var overlay = document.getElementById("search-overlay");
  var toggle = document.getElementById("search-toggle");
  if (!overlay || !toggle) return;

  var input = document.getElementById("search-input");
  var results = document.getElementById("search-results");
  var hint = document.getElementById("search-hint");
  var closeBtn = document.getElementById("search-close");

  var LUNR_SRC = "https://cdn.jsdelivr.net/npm/lunr@2.3.9/lunr.min.js";
  var INDEX_URL = overlay.getAttribute("data-search-url") || "/search.json";
  var IDLE_HINT = "Search across publications, the lab, essays, and posts.";

  var SUGGESTIONS = [
    { cat: "Research Lab",  title: "Interactive Lab Films",                          url: "/lab/" },
    { cat: "Publication",   title: "SecurePoL: Watermarking + Proof-of-Learning",    url: "/publication/2025-12-10-secureproofoflearning/" },
    { cat: "Publication",   title: "Feature-Based Model Watermarking for PoL",       url: "/publication/2024-11-01-ieee-access-watermarking/" },
    { cat: "Publication",   title: "Survey: Blockchain-Enhanced Machine Learning",   url: "/publication/2023-12-15-ieee-access-survey/" },
    { cat: "About",         title: "About Dr. Ozgur Ural",                           url: "/" },
    { cat: "Page",          title: "Projects & Experience",                          url: "/projects/" },
    { cat: "Field Notes",   title: "Technical Notes",                                url: "/blog/" },
    { cat: "Essays",        title: "Essays",                                         url: "/essays/" }
  ];

  var idx = null, store = [], state = "idle"; // idle | loading | ready | error
  var pending = false;

  function labelFor(url) {
    if (/\/publication\//.test(url)) return "Publication";
    if (/\/lab\//.test(url)) return "Research Lab";
    if (/\/posts\//.test(url)) return "Field Notes";
    if (url === "/" || /\/about/.test(url)) return "About";
    return "Page";
  }

  function esc(s) {
    return (s || "").replace(/[&<>"]/g, function (c) {
      return { "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;" }[c];
    });
  }

  function buildIndex(data) {
    store = data;
    idx = lunr(function () {
      this.ref("id");
      this.field("title", { boost: 12 });
      this.field("excerpt", { boost: 4 });
      this.field("content");
      var self = this;
      data.forEach(function (doc, i) { doc.id = i; self.add(doc); });
    });
    state = "ready";
    if (pending || input.value.trim()) { pending = false; runSearch(); }
    else { hint.textContent = IDLE_HINT; }
  }

  function loadIndex() {
    if (state === "ready" || state === "loading") return;
    state = "loading";
    hint.textContent = "Loading search…";
    var start = function () {
      fetch(INDEX_URL)
        .then(function (r) { return r.json(); })
        .then(buildIndex)
        .catch(function () { state = "error"; hint.textContent = "Search index failed to load."; });
    };
    if (window.lunr) { start(); return; }
    var s = document.createElement("script");
    s.src = LUNR_SRC;
    s.integrity = "sha384-203J0SNzyqHby3iU6hzvzltrWi/M41wOP5Gu+BiJMz5nwKykbkUx8Kp7iti0Lpli";
    s.crossOrigin = "anonymous";
    s.onload = start;
    s.onerror = function () { state = "error"; hint.textContent = "Could not load the search library."; };
    document.head.appendChild(s);
  }

  function render(matches, q) {
    results.innerHTML = "";
    if (!matches.length) { hint.textContent = "No results for “" + q + "”."; return; }
    hint.textContent = matches.length + " result" + (matches.length > 1 ? "s" : "") + " for “" + q + "”.";
    matches.slice(0, 12).forEach(function (m) {
      var doc = store[m.ref];
      if (!doc) return;
      var li = document.createElement("li");
      li.className = "search-results__item";
      li.innerHTML =
        '<a href="' + esc(doc.url) + '">' +
        '<span class="search-results__cat">' + esc(labelFor(doc.url)) + "</span>" +
        '<span class="search-results__title">' + esc(doc.title) + "</span>" +
        '<span class="search-results__excerpt">' + esc(doc.excerpt) + "</span>" +
        "</a>";
      results.appendChild(li);
    });
  }

  function renderSuggestions() {
    results.innerHTML = "";
    hint.textContent = IDLE_HINT;
    SUGGESTIONS.forEach(function (s) {
      var li = document.createElement("li");
      li.className = "search-results__item search-results__item--suggestion";
      li.innerHTML =
        '<a href="' + esc(s.url) + '">' +
        '<span class="search-results__cat">' + esc(s.cat) + '</span>' +
        '<span class="search-results__title">' + esc(s.title) + '</span>' +
        '</a>';
      results.appendChild(li);
    });
  }

  function runSearch() {
    var q = input.value.trim();
    if (!q) { renderSuggestions(); return; }
    if (state !== "ready") { pending = true; loadIndex(); return; }
    var matches = [];
    try { matches = idx.search(q); } catch (e) {}
    if (!matches.length) { try { matches = idx.search(q + "*"); } catch (e) {} }
    if (!matches.length) { try { matches = idx.search("*" + q + "*"); } catch (e) {} }
    render(matches, q);
  }

  function openSearch() {
    overlay.hidden = false;
    document.body.classList.add("search-open");
    loadIndex();
    if (!input.value.trim()) renderSuggestions();
    window.setTimeout(function () { input.focus(); input.select(); }, 40);
  }
  function closeSearch() {
    overlay.hidden = true;
    document.body.classList.remove("search-open");
  }

  toggle.addEventListener("click", function (e) { e.preventDefault(); openSearch(); });
  toggle.addEventListener("keydown", function (e) {
    if (e.key === "Enter" || e.key === " ") { e.preventDefault(); openSearch(); }
  });
  if (closeBtn) closeBtn.addEventListener("click", closeSearch);
  overlay.addEventListener("mousedown", function (e) { if (e.target === overlay) closeSearch(); });
  document.addEventListener("keydown", function (e) {
    if (e.key === "Escape" && !overlay.hidden) closeSearch();
  });
  document.addEventListener("keydown", function (e) {
    if (e.key !== "/" || !overlay.hidden) return;
    var tag = (e.target && e.target.tagName || "").toLowerCase();
    if (tag === "input" || tag === "textarea" || tag === "select" || (e.target && e.target.isContentEditable)) return;
    e.preventDefault(); openSearch();
  });

  var debounce;
  input.addEventListener("input", function () {
    window.clearTimeout(debounce);
    debounce = window.setTimeout(runSearch, 120);
  });
})();
